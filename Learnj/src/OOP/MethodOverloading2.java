package OOP;

public class MethodOverloading2 {


	public static void main(String[] args) {
		 System.out.println( MethodOverloading.add(2, 3));
		 System.out.println( MethodOverloading.add(2));
		 System.out.println( MethodOverloading.add(2, 3.2f));
		 System.out.println( MethodOverloading.add(2.2f, 3));
		 System.out.println( MethodOverloading.add(2.1f, 3.1f));
		 System.out.println( MethodOverloading.add(2, 3));
		

	}

}
